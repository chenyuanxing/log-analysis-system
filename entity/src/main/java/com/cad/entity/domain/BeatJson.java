package com.cad.entity.domain;

public class BeatJson {
    private String name;
    private String jsonFile;
    private String modulesJsonFile;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getJsonFile() {
        return jsonFile;
    }

    public void setJsonFile(String jsonFile) {
        this.jsonFile = jsonFile;
    }

    public String getModulesJsonFile() {
        return modulesJsonFile;
    }

    public void setModulesJsonFile(String modulesJsonFile) {
        this.modulesJsonFile = modulesJsonFile;
    }
}
